namespace Ducha\TelegramBot\Tests;

trait PrivateProtectedAwareTrait {
    /**
     * Call protected/private method of a class.
     *
     * @param object &$object    Instantiated object that we will run method on.
     * @param string $methodName Method name to call
     * @param array  $parameters Array of parameters to pass into method.
     *
     * @return mixed Method return.
     */
    public function invokeMethod(&$object, $methodName, array $parameters = array())
    {
        $reflection = new \ReflectionClass(get_class($object));
        $method = $reflection->getMethod($methodName);
        $method->setAccessible(true);

        return $method->invokeArgs($object, $parameters);
    }

    /**
     * Makes protected/private property of a class public
     * Something is wrong - don`t use it
     *
     * @param object &$object    Instantiated object whose property we will use.
     * @param string $propertyName Property name to make it public
     *
     */
    public function setPropertyPublic(&$object, $propertyName)
    {
        $reflection = new \ReflectionClass(get_class($object));
        $property = $reflection->getProperty($propertyName);
        $property->setAccessible(true);
    }
}